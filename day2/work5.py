# 足し算
def add(a, b):
    answer =a+b
    return answer

# 引き算
def sub(a,b):
    answer = a-b
    return answer

# 掛け算
def mul(a,b):
    answer = a*b
    return answer

# 割り算
def div(a,b):
    answer = a/b
    return answer

x = 1
y = 4
print(add(x, y))
print(sub(x, y))
print(mul(x, y))
print(div(x, y))